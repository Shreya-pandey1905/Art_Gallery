    // Global variable to track the current details overlay
let currentDetailsOverlay = null;

function showArtworkDetails(artworkId, category) {
    // Close any existing overlay first
    if (currentDetailsOverlay) {
        currentDetailsOverlay.remove();
    }

    // Select the appropriate data array based on category
    let artworkData;
    switch(category) {
        case 'paintings':
            artworkData = paintingsData;
            break;
        case 'sculptures':
            artworkData = sculpturesData;
            break;
        case 'photography':
            artworkData = photographyData;
            break;
        default:
            console.error('Invalid category');
            return;
    }
    
    // Find the specific artwork
    const artwork = artworkData.find(art => art.id === artworkId);
    
    if (!artwork) {
        console.error('Artwork not found');
        return;
    }

    // Create a modal or overlay for artwork details
    const detailsOverlay = document.createElement('div');
    detailsOverlay.className = 'artwork-details-overlay';
    detailsOverlay.innerHTML = `
        <div class="artwork-details-content">
            <button class="close-btn" onclick="closeArtworkDetails()">Ã—</button>
            <img src="${artwork.image}" alt="${artwork.title}" class="artwork-image">
            <div class="artwork-info">
                <h2>${artwork.title}</h2>
                <p class="artist">Artist: ${artwork.artist}</p>
                <p class="description">${artwork.description}</p>
                <p class="artist-bio">Artist Bio: ${artwork.bio}</p>
                <div class="pricing">
                    <p class="price">Price: $${artwork.price}</p>
                    <button class="buy-now" onclick="proceedToCheckout('${artwork.title}', ${artwork.price}, '${artwork.image}', '${artwork.artist}')">Buy Now</button>
                </div>
            </div>
        </div>
    `;

    // Add the overlay to the body
    document.body.appendChild(detailsOverlay);
    
    // Track the current overlay
    currentDetailsOverlay = detailsOverlay;
}

// Function to close artwork details
function closeArtworkDetails() {
    if (currentDetailsOverlay) {
        currentDetailsOverlay.remove();
        currentDetailsOverlay = null;
    }
}

// Function to proceed to checkout
function proceedToCheckout(title, price, image, artist) {
    // Store artwork details in localStorage for checkout page
    localStorage.setItem('checkoutArtwork', JSON.stringify({
        title: title,
        price: price,
        image: image,
        artist: artist
    }));

    // Redirect to checkout page
    window.location.href = 'checkout.html';
}

// Event listener for gallery items
document.addEventListener('DOMContentLoaded', () => {
    const galleryItems = document.querySelectorAll('.gallery-item');
    
    galleryItems.forEach(item => {
        item.addEventListener('click', (e) => {
            // Get artwork details from the clicked item
            const titleElement = item.querySelector('h3');
            if (!titleElement) {
                console.error('No title element found');
                return;
            }
            
            const title = titleElement.textContent;
            const artworkId = title.toLowerCase().replace(/\s+/g, '-');
            let category;

            // Determine category based on the gallery section
            const gallerySection = item.closest('.gallery');
            if (gallerySection.id === 'gallery') {
                category = 'paintings';
            } else if (gallerySection.id === 'Sculptures') {
                category = 'sculptures';
            } else if (gallerySection.id === 'Photography') {
                category = 'photography';
            } else {
                console.error('Unable to determine gallery category');
                return;
            }
            
            showArtworkDetails(artworkId, category);
        });
    });
});